    private void pesqbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesqbuttonActionPerformed
        Controller.ControlDBFuncionario funcdb = new Controller.ControlDBFuncionario();
        Model.ModelFuncionario func2 = new Model.ModelFuncionario();
        if ((campcodfunc.getText().equals("")) && (campnomefunc.getText().equals(""))){
           JOptionPane.showMessageDialog(null, "Por favor, insira os dados no campo", "Campo Vazio!", JOptionPane.ERROR_MESSAGE);
              }
        else{
            if(campcodfunc.getText().equals("")){
                try {
                    func2 = funcdb.buscaNome(campnomefunc.getText());
                } catch (SQLException ex) {
                    Logger.getLogger(FormPesquisaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
                }
                    abreForm(func2);
            }
            else{
                try {
                    func2 = funcdb.buscaCod(Integer.parseInt(campcodfunc.getText()));
                    abreForm(func2);
                } catch (SQLException ex) {
                    Logger.getLogger(FormPesquisaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_pesqbuttonActionPerformed

